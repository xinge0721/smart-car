// *************************** 例程常见问题说明 ***************************
// 遇到问题时请按照以下问题检查列表检查
//
// 问题1：串口没有数据
//      查看 逐飞助手 打开的是否是正确的串口，检查打开的 COM 口是否对应的是调试下载器
//      那么检查下载器线是否松动，检查核心板串口跳线是否已经焊接，串口跳线查看核心板原理图即可找到
//
// 问题2：串口数据乱码
//      查看 逐飞助手 设置的波特率是否与程序设置一致，程序中 zf_common_debug.h 文件中 DEBUG_UART_BAUDRATE 宏定义为 debug uart 使用的串口波特率
//
// 问题3：数据不连续发送，会有卡顿
//      查看接收机灯是否常亮，若接收机灯不规律闪，说明没有对接收机进行5v供电，信号微弱，需要为其提供稳定的5V电源
//
// 问题4：遥控器无法控制数据改变，或者没有数据
//      查看接收机灯是否常亮，若接收机灯不常亮，说明没有对接收机与遥控器没有配对，配对详见sbus接收机说明书

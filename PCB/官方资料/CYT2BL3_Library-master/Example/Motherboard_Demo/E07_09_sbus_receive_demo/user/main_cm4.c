// *************************** 例程硬件连接说明 ***************************
//      使用 sbus接收机 模块连接
//      模块管脚                                 单片机管脚
//      SBUS-TX             查看 zf_device_uart_receiver.h 文件中 UART_RECEVIER_RX_PIN 宏定义的引脚
//      SBUS-GND            核心板电源地 GND（需要与外部电源共地）
//      SBUS-5V             外接5V电源
//      SBUS-SBUS           悬空
//      SBUS-BAT+           电源正极 (3V3)
//      SBUS-BAT-           电源负极 (GND)

// *************************** 例程测试说明 ***************************
// 1.核心板烧录完成本例程，需要在断电情况下将调试下载器和SBUS接收机与单片机连接
//
// 2.将调试下载器连接电脑，为SBUS接收机提供5V电源完成上电(注意外部电源于核心板需要共地！！！)
//
// 3.将SBUS接收机与遥控器配对，先将长按SBUS接收机按键，等到绿灯连续闪烁，打开遥控器电源，等到接收机绿灯常亮，完成配对。（详见sbus接收机说明书）
//
// 4.电脑上使用 逐飞助手 打开对应的串口，串口波特率为 DEBUG_UART_BAUDRATE 宏定义 默认 115200，核心板按下复位按键
//
// 5.可以在 逐飞助手 上看到如下串口信息：
//                  CH1-CH6 data: ... ...
// 6.同时在遥控器上可以看见接收电压为3.3v左右，并且可以通过遥控器各个通道按键或旋钮更改通道数据， 逐飞助手 上可以看见对应的数据变化。
//
// 7.在正常输出串口数据后，将遥控器关闭可以看到如下串口信息：
//                  Remote control has been disconnected.
//
// 如果发现现象与说明严重不符 请参照本文件最下方 例程常见问题说明 进行排查

// **************************** 代码区域 ***************************
int main(void)
{
    clock_init(SYSTEM_CLOCK_160M); 	// 时钟配置及系统初始化<务必保留>
    debug_info_init();                  // 调试串口信息初始化
    
    // 此处编写用户代码 例如外设初始化代码等

	uart_receiver_init();                                            //sbus接收机初始化

    // 此处编写用户代码 例如外设初始化代码等
    while(true)
    {
        // 此处编写需要循环执行的代码

      
        if(1 == uart_receiver.finsh_flag)                            // 帧完成标志判断
        {
            if(1 == uart_receiver.state)                             // 遥控器失控状态判断
            {
                printf("CH1-CH6 data: ");
                for(int i = 0; i < 6; i++)
                {
                    printf("%d ", uart_receiver.channel[i]);         // 串口输出6个通道数据
                }
                printf("\r\n");
            }
            else
            {
                printf("Remote control has been disconnected.\r\n"); // 串口输出失控提示
            }
            uart_receiver.finsh_flag = 0;                            // 帧完成标志复位
        }  
      
      
        // 此处编写需要循环执行的代码
    }
}

